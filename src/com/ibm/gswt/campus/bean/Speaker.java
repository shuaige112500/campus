package com.ibm.gswt.campus.bean;

public class Speaker {

	private String id;
	
	private String eventId;
	
	private String speakerName;
	
	private String speakerDesc;
	
	private String speakerImg;
	
	private String speakerFile;
	
	private String topicName;
	
	private String topicDesc;
	
	private String startTime;
	
	private String endTime;
	
	private Integer rating;
	
	private String isRating;
	
	private String createTS;
	
	private String modifyTS;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getEventId() {
		return eventId;
	}

	public void setEventId(String eventId) {
		this.eventId = eventId;
	}

	public String getSpeakerName() {
		return speakerName;
	}

	public void setSpeakerName(String speakerName) {
		this.speakerName = speakerName;
	}

	public String getSpeakerDesc() {
		return speakerDesc;
	}

	public void setSpeakerDesc(String speakerDesc) {
		this.speakerDesc = speakerDesc;
	}

	public String getSpeakerImg() {
		return speakerImg;
	}

	public void setSpeakerImg(String speakerImg) {
		this.speakerImg = speakerImg;
	}

	public String getSpeakerFile() {
		return speakerFile;
	}

	public void setSpeakerFile(String speakerFile) {
		this.speakerFile = speakerFile;
	}

	public String getTopicName() {
		return topicName;
	}

	public void setTopicName(String topicName) {
		this.topicName = topicName;
	}

	public String getTopicDesc() {
		return topicDesc;
	}

	public void setTopicDesc(String topicDesc) {
		this.topicDesc = topicDesc;
	}

	public String getStartTime() {
		return startTime;
	}

	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}

	public String getEndTime() {
		return endTime;
	}

	public void setEndTime(String endTime) {
		this.endTime = endTime;
	}

	public Integer getRating() {
		return rating;
	}

	public void setRating(Integer rating) {
		this.rating = rating;
	}

	public String getIsRating() {
		return isRating;
	}

	public void setIsRating(String isRating) {
		this.isRating = isRating;
	}

	public String getCreateTS() {
		return createTS;
	}

	public void setCreateTS(String createTS) {
		this.createTS = createTS;
	}

	public String getModifyTS() {
		return modifyTS;
	}

	public void setModifyTS(String modifyTS) {
		this.modifyTS = modifyTS;
	}
}
